# una funcion puede tener dos tipos de parametros
    # obligatorios
    # optativos


# obligatorios

numero1 = 3
numero2 = 23

def sumar(numero1, numero2):
    resultado = numero1 + numero2
    print(resultado)

sumar(numero1, numero2)


# funcion de potencia 3 elevado a 4, 10 elevado a 6 y que valga para cualquier numero

def potencia(base, exponente):
    resultado = base ** exponente
    print(resultado)

potencia(3, 4)
potencia(10, 6)


# sacar la media de 3 numeros cualquiera
# la suma de tres numeros dividido entre la cantidad

def  media(n1, n2, n3):
    resultado = (n1 + n2 + n3)/3
    print(resultado)

media(2, 10, 20)


